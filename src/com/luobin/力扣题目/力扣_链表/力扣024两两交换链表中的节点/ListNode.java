/**
 * @author Doraemon
 * @date 2021/12/31 3:37 下午
 * @version 1.0
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode() {

    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
